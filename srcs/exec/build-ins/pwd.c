int	ft_pwd(char **env)
{
	int	j;

	j = 0;
	while (env[j])
	{
		if (ft_strncmp(env[j], "PWD", 3) == 0)
		{
			printf("%s\n", &env[j][4]);
			break;
		}	
		j++;
	}
	return (0);
}
